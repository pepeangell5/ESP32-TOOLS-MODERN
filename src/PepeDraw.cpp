// Ancho lógico de un carácter (en píxeles base, antes de multiplicar por size)
// Incluye 1 px de separación a la derecha
static int charAdvanceS(uint16_t cp) {
    GlyphS g;
    if (!lookupSmall(cp, g)) return 4;   // placeholder
    return g.width + 1;   // +1 px de separación
}

static int charAdvanceB(uint16_t cp) {
    GlyphB g;
    if (!lookupBig(cp, g)) return 5;
    return g.width + 1;
}

int getTextWidth(const String& txt, int size, FontType font) {
    int total = 0;
    int idx = 0;
    while (idx < (int)txt.length()) {
        uint16_t cp = nextCodepoint(txt, idx);
        total += (font == FONT_SMALL) ? charAdvanceS(cp) : charAdvanceB(cp);
    }
    return total * size;
}

int getFontHeight(int size, FontType font) {
    return ((font == FONT_SMALL) ? 7 : 12) * size;
}

String truncateToWidth(const String& txt, int maxWidth, int size, FontType font) {
    if (maxWidth <= 0) return "";
    if (getTextWidth(txt, size, font) <= maxWidth) return txt;

    const String ellipsis = "..";
    if (getTextWidth(ellipsis, size, font) >= maxWidth) return "";

    int idx = 0;
    int lastGood = 0;
    while (idx < (int)txt.length()) {
        nextCodepoint(txt, idx);
        String candidate = txt.substring(0, idx) + ellipsis;
        if (getTextWidth(candidate, size, font) > maxWidth) {
            return (lastGood > 0) ? txt.substring(0, lastGood) + ellipsis : ellipsis;
        }
        lastGood = idx;
    }

    return ellipsis;
}

// Renderizado genérico
static void drawStringGeneric(int x, int y, const String& txt,
                              uint16_t color, int size, FontType font) {
    int idx = 0;
    int cx = x;
    while (idx < (int)txt.length()) {
        uint16_t cp = nextCodepoint(txt, idx);
        if (font == FONT_SMALL) {
            GlyphS g;
            if (lookupSmall(cp, g)) {
                drawGlyphS(cx, y, g, color, size);
                cx += (g.width + 1) * size;
            } else {
                cx += 4 * size;   // carácter desconocido = espacio
            }
        } else {
            GlyphB g;
            if (lookupBig(cp, g)) {
                drawGlyphB(cx, y, g, color, size);
                cx += (g.width + 1) * size;
            } else {
                cx += 5 * size;
            }
        }
    }
}

// ── Compatibilidad hacia atrás ────────────────────────────────────────────
void drawCharCustom(int x, int y, char c, uint16_t color, int size) {
    GlyphS g;
    if (lookupSmall((uint16_t)(uint8_t)c, g)) {
        drawGlyphS(x, y, g, color, size);
    }
}

void drawStringCustom(int x, int y, String txt, uint16_t color, int size) {
    drawStringGeneric(x, y, txt, color, size, FONT_SMALL);
}

// ── API nueva ─────────────────────────────────────────────────────────────
void drawStringBig(int x, int y, const String& txt, uint16_t color, int size) {
    drawStringGeneric(x, y, txt, color, size, FONT_BIG);
}

void drawStringFit(int x, int y, const String& txt, uint16_t color,
                   int maxWidth, int size, FontType font) {
    String fitted = truncateToWidth(txt, maxWidth, size, font);
    drawStringGeneric(x, y, fitted, color, size, font);
}

void drawStringCentered(int y, const String& txt, uint16_t color,
                        int size, FontType font) {
    int w = getTextWidth(txt, size, font);
    int x = (320 - w) / 2;
    if (x < 0) x = 0;
    drawStringGeneric(x, y, txt, color, size, font);
}

void drawStringRight(int xRight, int y, const String& txt, uint16_t color,
                     int size, FontType font) {
    int w = getTextWidth(txt, size, font);
    int x = xRight - w;
    drawStringGeneric(x, y, txt, color, size, font);
}
