// ═══════════════════════════════════════════════════════════════════════════
//  UTF-8 DECODER
//  Devuelve el codepoint Unicode (0-0xFFFF) y avanza `idx` los bytes leídos
// ═══════════════════════════════════════════════════════════════════════════
static uint16_t nextCodepoint(const String& s, int& idx) {
    if (idx >= (int)s.length()) return 0;
    uint8_t b0 = (uint8_t)s[idx];

    // ASCII simple (1 byte)
    if (b0 < 0x80) {
        idx += 1;
        return b0;
    }

    // 2 bytes: 110xxxxx 10xxxxxx
    if ((b0 & 0xE0) == 0xC0 && idx + 1 < (int)s.length()) {
        uint8_t b1 = (uint8_t)s[idx + 1];
        idx += 2;
        return ((b0 & 0x1F) << 6) | (b1 & 0x3F);
    }

    // 3 bytes: 1110xxxx 10xxxxxx 10xxxxxx (raro, ignoramos bien)
    if ((b0 & 0xF0) == 0xE0 && idx + 2 < (int)s.length()) {
        uint8_t b1 = (uint8_t)s[idx + 1];
        uint8_t b2 = (uint8_t)s[idx + 2];
        idx += 3;
        return ((b0 & 0x0F) << 12) | ((b1 & 0x3F) << 6) | (b2 & 0x3F);
    }

    // Byte raro: avanzar y devolver '?'
    idx += 1;
    return '?';
}

// ═══════════════════════════════════════════════════════════════════════════
//  LOOKUP DE GLIFOS
// ═══════════════════════════════════════════════════════════════════════════

// Busca glifo SMALL. Devuelve puntero al glifo (del array PROGMEM) o nullptr.
// Llena `outGlyph` con una copia RAM si no es nullptr.
static bool lookupSmall(uint16_t cp, GlyphS& out) {
    // ASCII directo
    if (cp >= 0x20 && cp <= 0x7E) {
        memcpy_P(&out, &FONT_S_ASCII[cp - 0x20], sizeof(GlyphS));
        return true;
    }
    // Extensión español
    for (int i = 0; i < FONT_S_EXT_COUNT; i++) {
        uint16_t entryCp = pgm_read_word(&FONT_S_EXT[i].codepoint);
        if (entryCp == cp) {
            memcpy_P(&out, &FONT_S_EXT[i].g, sizeof(GlyphS));
            return true;
        }
    }
    return false;
}

// Igual para BIG
static bool lookupBig(uint16_t cp, GlyphB& out) {
    if (cp >= 0x20 && cp <= 0x7E) {
        memcpy_P(&out, &FONT_B_ASCII[cp - 0x20], sizeof(GlyphB));
        return true;
    }
    for (int i = 0; i < FONT_B_EXT_COUNT; i++) {
        uint16_t entryCp = pgm_read_word(&FONT_B_EXT[i].codepoint);
        if (entryCp == cp) {
            memcpy_P(&out, &FONT_B_EXT[i].g, sizeof(GlyphB));
            return true;
        }
    }
    return false;
}

// ═══════════════════════════════════════════════════════════════════════════
//  RENDERIZADO DE GLIFO (bit 0 = columna izquierda)
// ═══════════════════════════════════════════════════════════════════════════
static void drawGlyphS(int x, int y, const GlyphS& g, uint16_t color, int size) {
    for (int r = 0; r < 7; r++) {
        uint8_t row = g.rows[r];
        if (row == 0) continue;
        for (int c = 0; c < g.width; c++) {
            if (row & (1 << c)) {
                tft.fillRect(x + c * size, y + r * size, size, size, color);
            }
        }
    }
}

static void drawGlyphB(int x, int y, const GlyphB& g, uint16_t color, int size) {
    for (int r = 0; r < 12; r++) {
        uint8_t row = g.rows[r];
        if (row == 0) continue;
        for (int c = 0; c < g.width; c++) {
            if (row & (1 << c)) {
                tft.fillRect(x + c * size, y + r * size, size, size, color);
            }
        }
    }
}

